package com.project.kan.admin.vo;

public class Ward {

	private int ward_id;
	private int f_localbody_id;
	private String ward;
	private int ward_no;
	
	public int getWard_id() {
		return ward_id;
	}
	public void setWard_id(int ward_id) {
		this.ward_id = ward_id;
	}
	public int getF_localbody_id() {
		return f_localbody_id;
	}
	public void setF_localbody_id(int f_localbody_id) {
		this.f_localbody_id = f_localbody_id;
	}
	public String getWard() {
		return ward;
	}
	public void setWard(String ward) {
		this.ward = ward;
	}
	public int getWard_no() {
		return ward_no;
	}
	public void setWard_no(int ward_no) {
		this.ward_no = ward_no;
	}
	
}
